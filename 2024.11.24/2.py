def product(iterable):
    # Преобразуем итерируемый объект в список, чтобы избежать повторного его обхода
    numbers = list(iterable)
    
    # Базовый случай: если список пустой, возвращаем 1.0 (нейтральный элемент для умножения)
    if not numbers:
        return 1.0
    
    # Рекурсивный случай: умножаем первый элемент на произведение оставшихся
    return float(numbers[0]) * product(numbers[1:])

